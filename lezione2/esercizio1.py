# Mostra tutti i tipi di python 
a = 1
b = 1.0
c = "ciao"
d = True
e = [1,2,3]
f = (1,2,3)
g = {1,2,3}
h = {1:2, 3:4}
i = None

# Un int è un numero intero
print("a:", a, "è di tipo", type(a))

# Un float è un numero con la virgola
print("b:", b, "è di tipo", type(b))

# Una stringa è una sequenza di caratteri
print("c:", c, "è di tipo", type(c))

# Un booleano è un valore vero o falso (attenzione che a differenza di 
# altri linguaggi, True e False sono scritti con la prima lettera maiuscola)
print("d:", d, "è di tipo", type(d))

# Una lista è una sequenza di elementi
print("e:", e, "è di tipo", type(e))

# Una tupla è una sequenza di elementi immutabile
print("f:", f, "è di tipo", type(f))

# Un set è una sequenza di elementi non ordinata e senza duplicati
print("g:", g, "è di tipo", type(g))

# Un dizionario è una sequenza di coppie chiave-valore
print("h:", h, "è di tipo", type(h))

# None è un valore speciale che indica l'assenza di valore
print("i:", i, "è di tipo", type(i))
